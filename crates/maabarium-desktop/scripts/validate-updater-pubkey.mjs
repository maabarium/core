import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import process from "node:process";

function printHelp() {
  console.log(`Validate and normalize a Tauri updater public key.

Usage:
  pnpm validate:updater-pubkey -- --file ~/.tauri/maabarium.key.pub
  pnpm validate:updater-pubkey -- --value "<raw key line>"
  MAABARIUM_UPDATE_PUBKEY="<value>" pnpm validate:updater-pubkey

Options:
  --file <path>        Read the updater public key from a file
  --value <text>       Read the updater public key from an inline value
  --env-var <name>     Environment variable to read when --file/--value is omitted (default: MAABARIUM_UPDATE_PUBKEY)
  --github-env         Export normalized MAABARIUM_UPDATE_PUBKEY and MAABARIUM_UPDATE_PUBKEY_FILE entries to GITHUB_ENV
  --help               Show this help text

Notes:
  - Accepts either the raw Tauri updater public key line or the two-line .pub file contents.
  - Prints the recommended GitHub-safe value as the raw key line to avoid newline handling surprises.
`);
}

function parseArgs(argv) {
  const args = {
    file: null,
    value: null,
    envVar: "MAABARIUM_UPDATE_PUBKEY",
    githubEnv: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    const next = argv[index + 1];

    if (token === "--") {
      continue;
    }

    if (token === "--file" && next) {
      args.file = next;
      index += 1;
      continue;
    }

    if (token === "--value" && next) {
      args.value = next;
      index += 1;
      continue;
    }

    if (token === "--env-var" && next) {
      args.envVar = next;
      index += 1;
      continue;
    }

    if (token === "--github-env") {
      args.githubEnv = true;
      continue;
    }

    if (token === "--help") {
      printHelp();
      process.exit(0);
    }

    throw new Error(`Unknown argument: ${token}`);
  }

  if (args.file && args.value !== null) {
    throw new Error("Use either --file or --value, not both.");
  }

  return args;
}

function loadInput(args) {
  if (args.file) {
    const filePath = path.resolve(process.cwd(), args.file);
    return {
      sourceLabel: `file:${filePath}`,
      rawValue: fs.readFileSync(filePath, "utf8"),
    };
  }

  if (args.value !== null) {
    return {
      sourceLabel: "inline value",
      rawValue: args.value,
    };
  }

  const envValue = process.env[args.envVar];
  if (!envValue) {
    throw new Error(
      `No updater public key provided. Pass --file, --value, or set ${args.envVar}.`,
    );
  }

  return {
    sourceLabel: `env:${args.envVar}`,
    rawValue: envValue,
  };
}

function normalizePubkey(rawValue) {
  const normalized = rawValue
    .replace(/\r\n?/g, "\n")
    .replace(/\\n/g, "\n")
    .trim();
  const lines = normalized
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean);

  if (lines.length === 0 || lines.length > 2) {
    throw new Error(
      "Updater public key must be either the raw Tauri updater public key line or the two-line .pub file contents generated by `pnpm tauri signer generate`.",
    );
  }

  if (lines.length === 2 && !/^untrusted comment:/i.test(lines[0])) {
    throw new Error(
      "Updater public key contains two lines, but the first line is not the expected minisign comment header.",
    );
  }

  if (lines.some((line) => /^-----BEGIN|^-----END/.test(line))) {
    throw new Error(
      "Updater public key looks like a PEM block. Use the Tauri-generated minisign public key instead.",
    );
  }

  const keyLine = lines[lines.length - 1];
  if (!/^[A-Za-z0-9+/=]+$/.test(keyLine)) {
    throw new Error(
      "Updater public key is not valid base64. Use the Tauri-generated public key; do not use a Cloudflare key or a base64url variant.",
    );
  }

  return {
    normalizedLines: lines,
    keyLine,
    format: lines.length === 2 ? "two-line .pub file" : "raw key line",
  };
}

function exportGitHubEnv(normalizedLines) {
  const githubEnv = process.env.GITHUB_ENV;
  if (!githubEnv) {
    throw new Error(
      "GITHUB_ENV is not available. Only use --github-env inside GitHub Actions.",
    );
  }

  const keyLine = normalizedLines[normalizedLines.length - 1];
  const pubkeyFilePath = path.join(os.tmpdir(), "maabarium-updater.pub");
  fs.writeFileSync(pubkeyFilePath, `${normalizedLines.join("\n")}\n`, "utf8");
  const tauriConfigOverride = JSON.stringify({
    plugins: {
      updater: {
        pubkey: keyLine,
      },
    },
  });

  fs.appendFileSync(
    githubEnv,
    `MAABARIUM_UPDATE_PUBKEY_FILE=${pubkeyFilePath}\n`,
    "utf8",
  );
  fs.appendFileSync(
    githubEnv,
    "MAABARIUM_UPDATE_PUBKEY<<__MAABARIUM_EOF__\n",
    "utf8",
  );
  fs.appendFileSync(githubEnv, `${normalizedLines.join("\n")}\n`, "utf8");
  fs.appendFileSync(githubEnv, "__MAABARIUM_EOF__\n", "utf8");
  fs.appendFileSync(githubEnv, `TAURI_CONFIG=${tauriConfigOverride}\n`, "utf8");
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const { sourceLabel, rawValue } = loadInput(args);
  const { normalizedLines, keyLine, format } = normalizePubkey(rawValue);

  if (args.githubEnv) {
    exportGitHubEnv(normalizedLines);
  }

  console.log("Updater public key is valid.");
  console.log(`Source: ${sourceLabel}`);
  console.log(`Detected format: ${format}`);
  console.log(`Recommended GitHub variable value: ${keyLine}`);
  if (args.githubEnv) {
    console.log(
      "Exported normalized MAABARIUM_UPDATE_PUBKEY, MAABARIUM_UPDATE_PUBKEY_FILE, and TAURI_CONFIG to GITHUB_ENV.",
    );
  }
}

main();
